def verifier_langage(langage):
    if langage == "JavaScript":
        print("tu es un développeur web")
    elif langage == "python":
        print("tu es un développeur IA")
    elif langage == "java":
        print("tu es un développeur logiciel")
    elif langage == "reactjs":
        print("tu es un développeur mobile")
    else:
        print("un jour, je serai le meilleur développeur...")

# Appels de la fonction avec différents paramètres
verifier_langage("JavaScript")  # tu es un développeur web
verifier_langage("python")       # tu es un développeur IA
verifier_langage("java")         # tu es un développeur logiciel
verifier_langage("reactjs")      # tu es un développeur mobile
verifier_langage("C#")           # un jour, je serai le meilleur développeur